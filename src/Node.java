
public class Node {
	private String Name;
	private String IP;
	private int Port;

	public String getName() {
		return Name;
	}
	
	public void setName(String Name) {
		this.Name = Name;
	}
	
	public String getIP() {
		return IP;
	}
	
	public void setIP(String IP) {
		this.IP = IP;
	}
	
	public int getPort() {
		return Port; 
	}
	
	public void setPort(int Port) {
		this.Port = Port;
	}
}
